    /**
     * Возвращает список поддерживаемых в административной панели иконок
     *
     * @return array
     */
    public static function getIconsCssClasses()
    {
        return self::$zmdi;
    }

    /**
     * Возвращает список значений, подготовленных для отображения в выпадающем списке SELECT
     * У элемента Select, должен быть установлен CSS класс zmdi и задан атрибут size > 1 для корректного отображения иконок
     *
     * @return array
     */
    public static function getSelectList(array $first = [])
    {
        $result = [];
        foreach(self::getIconsCssClasses() as $css_class => $char_code) {
            $result[$css_class] = '&#x'.$char_code.';'.'&nbsp;&nbsp; '.str_replace('zmdi-', '', $css_class);
        }
        if (!empty($first)) {
            $result = $first + $result;
        }

        return $result;
    }

    /**
     * Возвращает код символа для HTML, соответствующего CSS классу
     *
     * @return string
     */
    public static function getCharCode($css_class)
    {
        return '&#x'.self::$zmdi[$css_class].';';
    }
